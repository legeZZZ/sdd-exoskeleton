describe("constitution edge cases", () => {
  it("handles project names with special markdown characters", () => {
    const config = makeConfig({
      project: { ...makeConfig().project, name: "my *awesome* _project_" },
    });
    const topology = makeEmptyTopology();

    const output = generateClaudeMd(config, topology);

    // Names should be escaped
    expect(output).toContain("# my \\*awesome\\* \\_project\\_");
  });

  it("handles project names with backticks", () => {
    const config = makeConfig({
      project: { ...makeConfig().project, name: "`evil` project" },
    });
    const topology = makeEmptyTopology();

    const output = generateClaudeMd(config, topology);

    expect(output).toContain("\\`evil\\` project");
  });

  it("handles empty language list gracefully", () => {
    const config = makeConfig({
      project: { ...makeConfig().project, languages: [] },
    });
    const topology = makeEmptyTopology();

    const output = generateClaudeMd(config, topology);

    // Should not crash, just have empty tech stack / coding standards
    expect(output).toContain("## Tech Stack");
    expect(output).toContain("## Coding Standards");
  });

  it("handles project with many modules (stress test)", () => {
    const modules: ModuleInfo[] = [];
    for (let i = 0; i < 100; i++) {
      modules.push({
        name: `module-${i}`,
        path: `src/module-${i}`,
        files: [`src/module-${i}/index.ts`],
        exports: [`export${i}`],
      });
    }
    const config = makeConfig();
    const topology = makeTopology(modules);

    expect(() => generateClaudeMd(config, topology)).not.toThrow();
    expect(() => generateAgentsMd(config, topology)).not.toThrow();

    const claudeOutput = generateClaudeMd(config, topology);
    // First module should appear (sorted: module-0)
    expect(claudeOutput).toContain("**module-0**");
    // Last by name (module-99)
    expect(claudeOutput).toContain("**module-99**");
  });

  it("generates idempotent output (calling twice gives same result)", () => {
    const config = makeConfig();
    const topology = makeTopology([makeSingleModule()]);

    const first = generateClaudeMd(config, topology);
    const second = generateClaudeMd(config, topology);

    expect(first).toBe(second);
  });

  it("AGENTS.md has simpler structure than CLAUDE.md", () => {
    const config = makeConfig();
    const topology = makeTopology([makeSingleModule()]);

    const claudeContent = generateClaudeMd(config, topology);
    const agentsContent = generateAgentsMd(config, topology);

    // AGENTS.md should be shorter than CLAUDE.md
    expect(agentsContent.length).toBeLessThan(claudeContent.length);
    // AGENTS.md should not contain workflow steps
    expect(agentsContent).not.toContain("Propose changes");
  });
});
