describe("generateClaudeMd", () => {
  it("includes the auto-generation marker", () => {
    const config = makeConfig();
    const topology = makeEmptyTopology();

    const output = generateClaudeMd(config, topology);

    expect(output).toContain("Auto-generated by sdd-exoskeleton");
  });

  it("includes the project name", () => {
    const config = makeConfig({ project: { ...makeConfig().project, name: "my-app" } });
    const topology = makeEmptyTopology();

    const output = generateClaudeMd(config, topology);

    expect(output).toContain("# my-app");
  });

  it("includes the project overview with source directory and languages", () => {
    const config = makeConfig();
    const topology = makeEmptyTopology();

    const output = generateClaudeMd(config, topology);

    expect(output).toContain("## Project Overview");
    expect(output).toContain("**Source:** `src`");
    expect(output).toContain("**Languages:** `typescript`");
  });

  it("includes tech stack section with language tooling", () => {
    const config = makeConfig({
      project: { ...makeConfig().project, languages: ["typescript", "python"] },
    });
    const topology = makeEmptyTopology();

    const output = generateClaudeMd(config, topology);

    expect(output).toContain("## Tech Stack");
    expect(output).toContain("### Typescript");
    expect(output).toContain("eslint");
    expect(output).toContain("prettier");
    expect(output).toContain("### Python");
    expect(output).toContain("ruff");
    expect(output).toContain("black");
  });

  it("handles unknown language gracefully in tech stack", () => {
    const config = makeConfig({
      project: { ...makeConfig().project, languages: ["typescript", "elixir"] },
    });
    const topology = makeEmptyTopology();

    expect(() => generateClaudeMd(config, topology)).not.toThrow();
    const output = generateClaudeMd(config, topology);
    expect(output).toContain("_No tooling configuration available._");
  });

  it("includes module map with sorted modules", () => {
    const modules: ModuleInfo[] = [
      makeSingleModule({ name: "database", path: "src/database", files: ["src/database/connection.ts"], exports: ["connect", "query"] }),
      makeSingleModule(), // auth — uses default 3 files, 3 exports
      makeSingleModule({ name: "utils", path: "src/utils", files: ["src/utils/helpers.ts"], exports: ["formatDate"] }),
    ];
    const config = makeConfig();
    const topology = makeTopology(modules);

    const output = generateClaudeMd(config, topology);

    expect(output).toContain("## Module Map");
    // Modules should appear alphabetically
    const authIdx = output.indexOf("**auth**");
    const dbIdx = output.indexOf("**database**");
    const utilsIdx = output.indexOf("**utils**");
    expect(authIdx).toBeLessThan(dbIdx);
    expect(dbIdx).toBeLessThan(utilsIdx);

    // Verify file and export counts
    expect(output).toContain("(3 files, 3 exports)"); // auth has 3 files, 3 exports
    expect(output).toContain("(1 files, 2 exports)"); // database has 1 file, 2 exports
    expect(output).toContain("(1 files, 1 exports)"); // utils has 1 file, 1 export
  });

  it("shows placeholder when no modules", () => {
    const config = makeConfig();
    const topology = makeEmptyTopology();

    const output = generateClaudeMd(config, topology);

    expect(output).toContain("_No modules indexed yet._");
  });

  it("includes coding standards section", () => {
    const config = makeConfig({
      project: { ...makeConfig().project, languages: ["typescript", "go"] },
    });
    const topology = makeEmptyTopology();

    const output = generateClaudeMd(config, topology);

    expect(output).toContain("## Coding Standards");
    expect(output).toContain("typescript/coding-style.md");
    expect(output).toContain("go/coding-style.md");
  });

  it("includes SDD workflow section", () => {
    const config = makeConfig();
    const topology = makeEmptyTopology();

    const output = generateClaudeMd(config, topology);

    expect(output).toContain("## SDD Workflow");
    expect(output).toContain("Specification-Driven Development (SDD)");
  });

  it("includes SDD commands", () => {
    const config = makeConfig();
    const topology = makeEmptyTopology();

    const output = generateClaudeMd(config, topology);

    expect(output).toContain("`sdd status`");
    expect(output).toContain("`sdd sync`");
    expect(output).toContain("`sdd doctor`");
  });

  it("includes openspec paths from config", () => {
    const config = makeConfig();
    const topology = makeEmptyTopology();

    const output = generateClaudeMd(config, topology);

    expect(output).toContain("openspec/specs/");
    expect(output).toContain("openspec/changes");
  });

  it("includes obsidian vault path from config", () => {
    const config = makeConfig();
    const topology = makeEmptyTopology();

    const output = generateClaudeMd(config, topology);

    expect(output).toContain("sdd-vault");
  });

  it("includes SDD workflow steps", () => {
    const config = makeConfig();
    const topology = makeEmptyTopology();

    const output = generateClaudeMd(config, topology);

    expect(output).toContain("Propose changes");
    expect(output).toContain("Implement after spec approval");
    expect(output).toContain("Archive completed changes");
  });

  it("does not mutate the config object", () => {
    const config = makeConfig();
    const originalName = config.project.name;
    const topology = makeTopology([makeSingleModule()]);

    generateClaudeMd(config, topology);

    expect(config.project.name).toBe(originalName);
  });

  it("does not mutate the topology object", () => {
    const config = makeConfig();
    const modules: ModuleInfo[] = [makeSingleModule()];
    const topology = makeTopology(modules);
    const originalModuleCount = topology.modules.length;

    generateClaudeMd(config, topology);

    expect(topology.modules.length).toBe(originalModuleCount);
  });

  it("includes all 5 major sections", () => {
    const config = makeConfig();
    const topology = makeTopology([makeSingleModule()]);

    const output = generateClaudeMd(config, topology);

    expect(output).toContain("## Project Overview");
    expect(output).toContain("## Tech Stack");
    expect(output).toContain("## Module Map");
    expect(output).toContain("## Coding Standards");
    expect(output).toContain("## SDD Workflow");
  });
});

