// Test fixtures
// ---------------------------------------------------------------------------

function makeConfig(overrides?: Partial<SddConfig>): SddConfig {
  return {
    version: "0.1.0",
    project: {
      name: "my-project",
      languages: ["typescript"],
      rootDir: "/app",
      srcDir: "src",
    },
    codegraph: {
      indexPath: ".codegraph",
      mcpPort: 0,
    },
    openspec: {
      path: "openspec",
      changeDir: "openspec/changes",
    },
    obsidian: {
      vaultPath: "sdd-vault",
      strategy: "hybrid",
    },
    sync: {
      mode: "manual",
      lastSyncRef: "",
      lastSyncAt: "",
    },
    ...overrides,
  };
}

function makeSingleModule(overrides?: Partial<ModuleInfo>): ModuleInfo {
  return {
    name: "auth",
    path: "src/auth",
    files: ["src/auth/login.ts", "src/auth/logout.ts", "src/auth/session.ts"],
    exports: ["login", "logout", "validateSession"],
    ...overrides,
  };
}

function makeTopology(modules: ModuleInfo[]): ModuleTopology {
  return {
    modules,
    edges: [],
    entryPoints: [],
  };
}

function makeEmptyTopology(): ModuleTopology {
  return {
    modules: [],
    edges: [],
    entryPoints: [],
  };
}

