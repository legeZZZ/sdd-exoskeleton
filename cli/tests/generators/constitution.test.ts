describe("writeConstitution", () => {
  let tmpDir: string;

  beforeEach(() => {
    tmpDir = createTmpDir();
  });

  afterEach(() => {
    fs.rmSync(tmpDir, { recursive: true, force: true });
  });

  it("writes both CLAUDE.md and AGENTS.md to the project directory", () => {
    const config = makeConfig();
    const topology = makeEmptyTopology();

    const result = writeConstitution(tmpDir, config, topology);

    expect(fileExists(path.join(tmpDir, "CLAUDE.md"))).toBe(true);
    expect(fileExists(path.join(tmpDir, "AGENTS.md"))).toBe(true);
    expect(result.written).toContain(path.join(tmpDir, "CLAUDE.md"));
    expect(result.written).toContain(path.join(tmpDir, "AGENTS.md"));
    expect(result.skipped).toEqual([]);
  });

  it("returns correct result with written and skipped lists", () => {
    const config = makeConfig();
    const topology = makeEmptyTopology();

    const result = writeConstitution(tmpDir, config, topology);

    expect(result.written).toHaveLength(2);
    expect(result.skipped).toHaveLength(0);
  });

  it("overwrites existing files that contain the auto-generation marker", () => {
    const originalContent = `# Old Content\n\n> Auto-generated by sdd-exoskeleton\n\nOld content here.`;
    fs.writeFileSync(path.join(tmpDir, "CLAUDE.md"), originalContent, "utf-8");

    const config = makeConfig();
    const topology = makeEmptyTopology();

    writeConstitution(tmpDir, config, topology);

    const newContent = readFile(path.join(tmpDir, "CLAUDE.md"));
    expect(newContent).not.toBe(originalContent);
    expect(newContent).toContain("## Project Overview");
  });

  it("skips existing files that do NOT contain the auto-generation marker", () => {
    const userContent = "# My Custom CLAUDE.md\n\nHand-written content.";
    fs.writeFileSync(path.join(tmpDir, "CLAUDE.md"), userContent, "utf-8");

    const config = makeConfig();
    const topology = makeEmptyTopology();

    const result = writeConstitution(tmpDir, config, topology);

    // CLAUDE.md should NOT be overwritten
    expect(readFile(path.join(tmpDir, "CLAUDE.md"))).toBe(userContent);
    // AGENTS.md should still be written
    expect(fileExists(path.join(tmpDir, "AGENTS.md"))).toBe(true);

    expect(result.skipped).toContain(path.join(tmpDir, "CLAUDE.md"));
    expect(result.written).toContain(path.join(tmpDir, "AGENTS.md"));
  });

  it("skips AGENTS.md when it exists without marker", () => {
    const userContent = "# My Custom AGENTS.md\n\nHand-written.";
    fs.writeFileSync(path.join(tmpDir, "AGENTS.md"), userContent, "utf-8");

    const config = makeConfig();
    const topology = makeEmptyTopology();

    const result = writeConstitution(tmpDir, config, topology);

    expect(readFile(path.join(tmpDir, "AGENTS.md"))).toBe(userContent);
    expect(result.skipped).toContain(path.join(tmpDir, "AGENTS.md"));
    expect(result.written).toContain(path.join(tmpDir, "CLAUDE.md"));
  });

  it("skips both files when both exist without marker", () => {
    const claudeContent = "# Custom CLAUDE.md\n\nCustom.";
    const agentsContent = "# Custom AGENTS.md\n\nCustom.";
    fs.writeFileSync(path.join(tmpDir, "CLAUDE.md"), claudeContent, "utf-8");
    fs.writeFileSync(path.join(tmpDir, "AGENTS.md"), agentsContent, "utf-8");

    const config = makeConfig();
    const topology = makeEmptyTopology();

    const result = writeConstitution(tmpDir, config, topology);

    expect(readFile(path.join(tmpDir, "CLAUDE.md"))).toBe(claudeContent);
    expect(readFile(path.join(tmpDir, "AGENTS.md"))).toBe(agentsContent);
    expect(result.written).toEqual([]);
    expect(result.skipped).toHaveLength(2);
  });

  it("writes with module data when modules are provided", () => {
    const config = makeConfig();
    const modules: ModuleInfo[] = [
      makeSingleModule(),
      makeSingleModule({ name: "utils", path: "src/utils", files: ["src/utils/helpers.ts"], exports: ["formatDate"] }),
    ];
    const topology = makeTopology(modules);

    writeConstitution(tmpDir, config, topology);

    const claudeContent = readFile(path.join(tmpDir, "CLAUDE.md"));
    expect(claudeContent).toContain("**auth**");
    expect(claudeContent).toContain("**utils**");
  });

  it("writes with multiple languages in tech stack", () => {
    const config = makeConfig({
      project: { ...makeConfig().project, languages: ["typescript", "go", "rust"] },
    });
    const topology = makeEmptyTopology();

    writeConstitution(tmpDir, config, topology);

    const claudeContent = readFile(path.join(tmpDir, "CLAUDE.md"));
    expect(claudeContent).toContain("### Typescript");
    expect(claudeContent).toContain("### Go");
    expect(claudeContent).toContain("### Rust");
  });

  it("handles empty modules gracefully", () => {
    const config = makeConfig();
    const topology = makeEmptyTopology();

    expect(() => writeConstitution(tmpDir, config, topology)).not.toThrow();
    expect(fileExists(path.join(tmpDir, "CLAUDE.md"))).toBe(true);
  });

  it("does not mutate config or topology during write", () => {
    const config = makeConfig();
    const modules: ModuleInfo[] = [makeSingleModule()];
    const topology = makeTopology(modules);
    const originalName = config.project.name;
    const originalModuleCount = topology.modules.length;

    writeConstitution(tmpDir, config, topology);

    expect(config.project.name).toBe(originalName);
    expect(topology.modules.length).toBe(originalModuleCount);
  });

  it("CLAUDE.md contains all expected top-level sections", () => {
    const config = makeConfig();
    const topology = makeTopology([makeSingleModule()]);

    writeConstitution(tmpDir, config, topology);

    const content = readFile(path.join(tmpDir, "CLAUDE.md"));
    expect(content).toContain("## Project Overview");
    expect(content).toContain("## Tech Stack");
    expect(content).toContain("## Module Map");
    expect(content).toContain("## Coding Standards");
    expect(content).toContain("## SDD Workflow");
  });

  it("AGENTS.md contains all expected top-level sections", () => {
    const config = makeConfig();
    const topology = makeTopology([makeSingleModule()]);

    writeConstitution(tmpDir, config, topology);

    const content = readFile(path.join(tmpDir, "AGENTS.md"));
    expect(content).toContain("## Project Context");
    expect(content).toContain("## Module Structure");
    expect(content).toContain("## SDD Commands");
    expect(content).toContain("## Coding Rules");
  });
});

