/**
 * Write CLAUDE.md and AGENTS.md to the project directory.
 *
 * Existing files are only overwritten if they contain the sdd-exoskeleton
 * auto-generation marker.  Files that exist without the marker are skipped.
 *
 * Returns a result listing which files were written and which were skipped.
 */
export function writeConstitution(
  projectDir: string,
  config: SddConfig,
  topology: ModuleTopology,
): WriteResult {
  const written: string[] = [];
  const skipped: string[] = [];

  const claudePath = path.join(projectDir, "CLAUDE.md");
  const agentsPath = path.join(projectDir, "AGENTS.md");

  const claudeContent = generateClaudeMd(config, topology);
  writeIfAllowed(claudePath, claudeContent, written, skipped);

  const agentsContent = generateAgentsMd(config, topology);
  writeIfAllowed(agentsPath, agentsContent, written, skipped);

  return { written, skipped };
}

// ---------------------------------------------------------------------------
// Private helpers
// ---------------------------------------------------------------------------

/**
 * Write content to a file path only if the file does not exist or already
 * contains the auto-generation marker.  Updates the written / skipped lists
 * in place.
 */
function writeIfAllowed(
  filePath: string,
  content: string,
  written: string[],
  skipped: string[],
): void {
  const existing = safeRead(filePath);

  if (existing !== null && !existing.includes(AUTO_GEN_MARKER)) {
    skipped.push(filePath);
    return;
  }

  safeWrite(filePath, content);
  written.push(filePath);
}

/** Escape markdown special characters so raw values render literally. */
function escapeMd(value: string): string {
  return value
    .replace(/\\/g, "\\\\")
    .replace(/`/g, "\\`")
    .replace(/\*/g, "\\*")
    .replace(/_/g, "\\_")
    .replace(/\[/g, "\\[")
    .replace(/\]/g, "\\]")
    .replace(/</g, "\\<")
    .replace(/>/g, "\\>");
}

/** Capitalize the first character of a string. */
function capitalize(value: string): string {
  if (value.length === 0) return value;
  return value[0].toUpperCase() + value.slice(1);
}
