/**
 * Render an AGENTS.md file for OpenCode / AI agent instructions.
 *
 * Includes project context, module structure, SDD commands, and coding rules.
 */
export function generateAgentsMd(
  config: SddConfig,
  topology: ModuleTopology,
): string {
  const lines: string[] = [];

  lines.push(`# ${escapeMd(config.project.name)} — AI Agent Instructions`);
  lines.push("");
  lines.push(`> ${AUTO_GEN_MARKER}`);
  lines.push("");

  // -- Project Context ---------------------------------------------------------
  lines.push("## Project Context");
  lines.push("");
  const langList = config.project.languages
    .map((l) => `\`${escapeMd(l)}\``)
    .join(", ");
  lines.push(`This is a ${langList} project. Source code is in \`${escapeMd(config.project.srcDir)}\`.`);
  lines.push("");

  // -- Module Structure --------------------------------------------------------
  lines.push("## Module Structure");
  lines.push("");
  if (topology.modules.length > 0) {
    const sorted = [...topology.modules].sort((a, b) =>
      a.name.localeCompare(b.name),
    );
    for (const mod of sorted) {
      lines.push(`- **${escapeMd(mod.name)}** — \`${escapeMd(mod.path)}\``);
    }
  } else {
    lines.push("_No modules indexed yet._");
  }
  lines.push("");

  // -- SDD Commands ------------------------------------------------------------
  lines.push("## SDD Commands");
  lines.push("");
  lines.push("Run `sdd` commands to interact with the SDD tooling:");
  lines.push("- `sdd status` — current project state");
  lines.push("- `sdd sync` — sync changes");
  lines.push("- `sdd doctor` — health check");
  lines.push("");

  // -- Coding Rules ------------------------------------------------------------
  lines.push("## Coding Rules");
  lines.push("");
  for (const lang of config.project.languages) {
    try {
      const langConfig = getLanguageConfig(lang);
      lines.push(
        `- **${capitalize(lang)}:** lint with \`${escapeMd(langConfig.linter)}\`, format with \`${escapeMd(langConfig.formatter)}\``,
      );
    } catch {
      lines.push(`- **${capitalize(lang)}:** _no tooling configured_`);
    }
  }
  lines.push("");

  return lines.join("\n");
}

